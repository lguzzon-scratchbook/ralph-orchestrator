"""Basic math operations module.

Provides simple arithmetic functions for demonstration and testing purposes.
"""


def add(a: float, b: float) -> float:
    """Add two numbers together.

    Args:
        a: The first number to add.
        b: The second number to add.

    Returns:
        The sum of a and b.

    Examples:
        >>> add(2, 3)
        5
        >>> add(-1, 1)
        0
    """
    return a + b
